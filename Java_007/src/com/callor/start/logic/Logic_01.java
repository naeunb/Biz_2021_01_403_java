package com.callor.start.logic;

/*
 * 논리(Logic) 연산
 * 참(yes,true),거짓(no,false)을 판단하여 어떤 행위를 수행하는 연산
 */
public class Logic_01 {
	public static void main(String[] args) {
		
		System.out.println(3<3);
		System.out.println(3>3);
		
		System.out.println(3==3);

		System.out.println(3<=3);
		
		System.out.println(3>=3);
	}
}
